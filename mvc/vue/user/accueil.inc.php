        Ce site propose certaines fonctionnalités qui ne sont accessibles qu'aux personnes ayant un compte. 3 niveaux de compte sont proposés.
        <h3>Le compte visiteur</h3> 
        Le compte visiteur est accessible à tout le monde. La création d'un compte s'effectue très facilement à partir du <a href="./index.php?module=user&page=inscription" class="info">formulaire d'inscription</a>. Une fois inscrites, 
        les personnes ayant un compte visiteur ont la possibilité de visualiser les films proposés à nos membres. Les personnes ayant un compte visiteur ont aussi la 
        possibilité de gérer leur compte : modification des informations personnelles, modification du mot de passe, choix de l'avatar, ... Par contre, elles n'ont pas 
        la possibilité d'évaluer et de commenter les films proposés par notre cinémathèque, ces fonctionnalités étant réservées aux membres de notre association.
        <h3>Le compte membre</h3> 
        Le compte membre n'est accessible qu'aux membres de l'association ayant acquittés leur cotisation annuelle. Un compte membre offre toutes les fonctionnalités d'un compte visiteur avec en plus la possibilité d'évaluer et de commenter les films proposés par notre cinémathèque. D'autres fonctionnalités seront implémentées prochainement et seront aussi réservées uniquement à nos membres.
        <h3>Le compte administrateur</h3> 
        Le compte administrateur n'est accessible qu'à quelques personnes seulement. Il permet d'administrer le site et donne accès à certaines statistiques sous forme de graphiques. L'administrateur a tous les droits, ce qui justifie qu'un compte de ce type ne soit réservé qu'à quelques personnes seulement.
        Bien évidemment, il est possible à tout moment de clôturer son compte très facilement en cliquant sur le lien approprié.

